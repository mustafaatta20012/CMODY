window.i18n = {
  currentLang: localStorage.getItem("gm_lang") || "en",

  toggle: function () {
    this.setLanguage(this.currentLang === "en" ? "ar" : "en");
  },

  setLanguage: function (lang) {
    this.currentLang = lang;
    localStorage.setItem("gm_lang", lang);

    document.documentElement.lang = lang;
    if (lang === "ar") {
      document.documentElement.dir = "rtl";
      document.body.classList.add("rtl-mode");
    } else {
      document.documentElement.dir = "ltr";
      document.body.classList.remove("rtl-mode");
    }

    const toggleBtn = document.getElementById("lang-toggle-btn");
    if (toggleBtn) {
      toggleBtn.textContent = lang === "ar" ? "🌐 العربية" : "🌐 English";
    }

    this.translateNode(document.body);

    window.dispatchEvent(new CustomEvent("languageChanged", { detail: { lang } }));
  },

  translateNode: function (rootElement) {
    const isAr = this.currentLang === "ar";
    const dict = translations["ar"] || {};

    const elements = [rootElement, ...rootElement.querySelectorAll('*')];
    elements.forEach(el => {
      if (['SCRIPT', 'STYLE', 'HTML', 'HEAD', 'META', 'LINK'].includes(el.tagName)) return;

      if (el.placeholder) {
        if (typeof el.origPlaceholder === 'undefined') {
          el.origPlaceholder = el.placeholder;
        }
        const key = el.origPlaceholder;
        if (isAr && dict[key]) {
          el.placeholder = dict[key];
        } else if (!isAr) {
          el.placeholder = el.origPlaceholder;
        }
      }

      Array.from(el.childNodes).forEach(node => {
        if (node.nodeType === 3) {
          this.translateTextNode(node);
        }
      });
    });
  },

  translateTextNode: function (node) {
    const text = node.nodeValue.trim();
    if (!text) return;

    const isAr = this.currentLang === "ar";
    const dict = translations["ar"] || {};

    if (typeof node.origText !== 'undefined') {
      const expectedText = isAr ? (dict[node.origText] || node.origText) : node.origText;
      if (text !== expectedText && text !== dict[node.origText]) {
        // Prevent resetting original text if it matches active dynamic translations in either language
        const isAlreadyTranslatedAr = isAr && (
          (text.startsWith("✅ ") && text.endsWith(" إلى السلة")) ||
          (text.startsWith("🗑 ") && text.endsWith(" من السلة")) ||
          (text.startsWith("🎁 ") && text.endsWith("!")) ||
          text.startsWith("❌ ") ||
          text.startsWith("⚠️ ") ||
          text.startsWith("⚠ ")
        );
        const isAlreadyTranslatedEn = !isAr && (
          (node.origText.startsWith("✅ ") && node.origText.endsWith(" added to cart") && text.startsWith("✅ ") && text.endsWith(" إلى السلة")) ||
          (node.origText.startsWith("🗑 ") && node.origText.endsWith(" removed") && text.startsWith("🗑 ") && text.endsWith(" من السلة")) ||
          (node.origText.startsWith("🎁 ") && node.origText.endsWith("% discount applied!") && text.startsWith("🎁 ")) ||
          (node.origText.startsWith("❌ ") && text.startsWith("❌ ")) ||
          (node.origText.startsWith("⚠️ ") && text.startsWith("⚠️ ")) ||
          (node.origText.startsWith("⚠ ") && text.startsWith("⚠ "))
        );

        if (isAlreadyTranslatedAr || isAlreadyTranslatedEn) {
          // Do not reset origText
        } else {
          node.origText = text;
        }
      }
    } else {
      node.origText = text;
    }

    const key = node.origText;

    if (isAr) {
      if (dict[key]) {
        node.nodeValue = node.nodeValue.replace(node.origText, dict[key]);
      } else {
        // Fallback: Dynamic prefix/suffix translation for notifications
        let translated = key;

        if (key.startsWith("✅ ") && key.endsWith(" added to cart")) {
          const prodName = key.slice(2, -14);
          const transProdName = dict[prodName] || prodName;
          translated = `✅ تم إضافة ${transProdName} إلى السلة`;
        }
        else if (key.startsWith("🗑 ") && key.endsWith(" removed")) {
          const prodName = key.slice(2, -8);
          const transProdName = dict[prodName] || prodName;
          translated = `🗑 تم إزالة ${transProdName} من السلة`;
        }
        else if (key.startsWith("🎁 ") && key.endsWith("% discount applied!")) {
          const discount = key.slice(2, -19);
          translated = `🎁 تم تطبيق خصم ${discount}%!`;
        }
        else if (key.startsWith("❌ Failed to place order: ")) {
          const err = key.slice(26);
          translated = `❌ فشل تقديم الطلب: ${err}`;
        }
        else if (key.startsWith("❌ ")) {
          const rest = key.slice(2);
          const transRest = dict[rest] || rest;
          translated = `❌ ${transRest}`;
        }
        else if (key.startsWith("⚠️ ")) {
          const rest = key.slice(3);
          const transRest = dict[rest] || rest;
          translated = `⚠️ ${transRest}`;
        }
        else if (key.startsWith("⚠ ")) {
          const rest = key.slice(2);
          const transRest = dict[rest] || rest;
          translated = `⚠ ${transRest}`;
        }

        if (translated !== key) {
          node.nodeValue = node.nodeValue.replace(node.origText, translated);
        }
      }
    } else if (!isAr && node.origText) {
      node.nodeValue = node.nodeValue.replace(text, node.origText);
    }
  },

  getText: function (key) {
    if (this.currentLang === "ar" && translations["ar"][key]) {
      return translations["ar"][key];
    }
    return key;
  }
};

document.addEventListener("DOMContentLoaded", () => {
  i18n.setLanguage(i18n.currentLang);

  const observer = new MutationObserver((mutations) => {
    observer.disconnect();
    mutations.forEach(m => {
      if (m.type === 'childList') {
        m.addedNodes.forEach(node => {
          if (node.nodeType === 1) { // Element
            i18n.translateNode(node);
          } else if (node.nodeType === 3) { // Text
            i18n.translateTextNode(node);
          }
        });
      } else if (m.type === 'characterData') {
        i18n.translateTextNode(m.target);
      }
    });
    observe();
  });

  function observe() {
    observer.observe(document.body, { childList: true, subtree: true, characterData: true });
  }

  observe();
});
